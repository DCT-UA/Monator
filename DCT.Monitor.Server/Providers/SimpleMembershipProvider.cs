            _userModule.Create(user);
            status = MembershipCreateStatus.Success;

            return new MembershipUser("SimpleMembershipProvider", username, user.Id, email, null, null, true, false, DateTime.Now, DateTime.Now, DateTime.Now, DateTime.Now, DateTime.Now);
        }

